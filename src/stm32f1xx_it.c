/**
  * @brief  EXTI line detection callbacks.
  * @param  GPIO_Pin: Specifies the pins connected EXTI line
  * @retval None
  */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
  /* Prevent unused argument(s) compilation warning */
  UNUSED(GPIO_Pin);

  static uint32_t last_enable_press = 0;
  static uint32_t last_disable_press = 0;
  uint32_t current_tick = HAL_GetTick();

  /* Button debouncing - 50ms minimum between presses */
  if (GPIO_Pin == BUTTON_ENABLE_Pin)
  {
    if ((current_tick - last_enable_press) > 50)
    {
      button_enable_pressed = 1;
      last_enable_press = current_tick;
    }
  }
  else if (GPIO_Pin == BUTTON_DISABLE_Pin)
  {
    if ((current_tick - last_disable_press) > 50)
    {
      button_disable_pressed = 1;
      last_disable_press = current_tick;
    }
  }
}